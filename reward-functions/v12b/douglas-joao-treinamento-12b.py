def reward_function(params):
    if params["is_offtrack"]:
        return -1.0

    if not params["all_wheels_on_track"]:
        return 1e-3

    x = params["x"]
    y = params["y"]
    speed = params["speed"]
    heading = params["heading"]
    progress = params["progress"]
    steps = params["steps"]
    track_width = params["track_width"]

    # 1) Ponto mais próximo da racing line
    n = len(RACING_LINE)
    min_dist = float("inf")
    closest_idx = 0
    for i in range(n):
        dx = x - RACING_LINE[i][0]
        dy = y - RACING_LINE[i][1]
        d = (dx * dx + dy * dy) ** 0.5
        if d < min_dist:
            min_dist = d
            closest_idx = i

    target_spd = RACING_LINE[closest_idx][2]

    # 2) Distância da racing line
    line_reward = max(1.0 - (min_dist / (track_width * 0.5)), 0.0)

    # 3) Velocidade alvo — penalidade suave
    speed_diff = abs(speed - target_spd)
    speed_reward = max(1.0 - (speed_diff / 2.67), 0.1)
    if speed_diff < 0.01:
        speed_reward += 0.5

    # 4) Heading alinhado com a racing line
    next_idx = (closest_idx + 1) % n
    rl_dir = math.degrees(math.atan2(
        RACING_LINE[next_idx][1] - RACING_LINE[closest_idx][1],
        RACING_LINE[next_idx][0] - RACING_LINE[closest_idx][0]
    ))
    direction_diff = abs(rl_dir - heading)
    if direction_diff > 180:
        direction_diff = 360 - direction_diff
    heading_reward = max(1.0 - direction_diff / 30.0, 0.0)

    # 5) Reward ponderado
    reward = (
        0.40 * speed_reward
        + 0.35 * line_reward
        + 0.25 * heading_reward
    )

    reward += progress / 100.0

    if progress >= 99.0:
        reward += 100.0 * (100.0 / max(steps, 1)) ** 3

    return float(max(reward, 1e-3))
